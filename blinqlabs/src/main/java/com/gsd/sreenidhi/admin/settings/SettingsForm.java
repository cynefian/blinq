package com.gsd.sreenidhi.admin.settings;

public class SettingsForm {
	private String tx_category_name;
	private String blog_category_count;

	public String getTx_category_name() {
		return tx_category_name;
	}

	public void setTx_category_name(String tx_category_name) {
		this.tx_category_name = tx_category_name;
	}

	public String getBlog_category_count() {
		return blog_category_count;
	}

	public void setBlog_category_count(String blog_category_count) {
		this.blog_category_count = blog_category_count;
	}
	
	

}
