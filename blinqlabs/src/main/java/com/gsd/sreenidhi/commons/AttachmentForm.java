package com.gsd.sreenidhi.commons;

public class AttachmentForm {
	
	public int ID;
	public int ID_LINK;
	public String TX_DATA;
	public String TX_NAME;
	public String TX_TYPE;
	public String TX_SIZE;
	public int attachCount;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getID_LINK() {
		return ID_LINK;
	}
	public void setID_LINK(int iD_LINK) {
		ID_LINK = iD_LINK;
	}
	public String getTX_DATA() {
		return TX_DATA;
	}
	public void setTX_DATA(String tX_DATA) {
		TX_DATA = tX_DATA;
	}
	public String getTX_NAME() {
		return TX_NAME;
	}
	public void setTX_NAME(String tX_NAME) {
		TX_NAME = tX_NAME;
	}
	public String getTX_TYPE() {
		return TX_TYPE;
	}
	public void setTX_TYPE(String tX_TYPE) {
		TX_TYPE = tX_TYPE;
	}
	public String getTX_SIZE() {
		return TX_SIZE;
	}
	public void setTX_SIZE(String tX_SIZE) {
		TX_SIZE = tX_SIZE;
	}
	public int getAttachCount() {
		return attachCount;
	}
	public void setAttachCount(int attachCount) {
		this.attachCount = attachCount;
	}
	
	
	

}
