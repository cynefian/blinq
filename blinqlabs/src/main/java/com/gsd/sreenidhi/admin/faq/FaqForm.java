package com.gsd.sreenidhi.admin.faq;

public class FaqForm {
	
	int ID;
	String TX_QUESTION;
	String TX_ANSWER;
	boolean FL_ACTIVE;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getTX_QUESTION() {
		return TX_QUESTION;
	}
	public void setTX_QUESTION(String tX_QUESTION) {
		TX_QUESTION = tX_QUESTION;
	}
	public String getTX_ANSWER() {
		return TX_ANSWER;
	}
	public void setTX_ANSWER(String tX_ANSWER) {
		TX_ANSWER = tX_ANSWER;
	}
	public boolean isFL_ACTIVE() {
		return FL_ACTIVE;
	}
	public void setFL_ACTIVE(boolean fL_ACTIVE) {
		FL_ACTIVE = fL_ACTIVE;
	}
	
	

}
